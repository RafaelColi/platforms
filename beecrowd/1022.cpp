#include <bits/stdc++.h>

using namespace std;





int main() {
    int a, b, c, d;
    char op;
    int n;

    cin >> n;

    for(int i = 0; i < n; i++) {
        scanf("%d %*c %d %c %d %*c %d", &a, &b, &op, &c, &d);

        switch(op)  {
            case '+':
                break;
            
            case '-':
                break;

            case '*':
                break;

            case '/':
                break;
        }
    }
    
    

    

    return 0;
}